package com.Staff;

public class AssignEmp {
	
	private int shAssignIdD;
	private int shiftID;
	private int empID;
	
	
	public AssignEmp(int shAssignIdD, int shiftID, int empID) {
		super();
		this.shAssignIdD = shAssignIdD;
		this.shiftID = shiftID;
		this.empID = empID;
	}
	public int getShAssignIdD() {
		return shAssignIdD;
	}
	public void setShAssignIdD(int shAssignIdD) {
		this.shAssignIdD = shAssignIdD;
	}
	public int getShiftID() {
		return shiftID;
	}
	public void setShiftID(int shiftID) {
		this.shiftID = shiftID;
	}
	public int getEmpID() {
		return empID;
	}
	public void setEmpID(int empID) {
		this.empID = empID;
	}
	
	

}
